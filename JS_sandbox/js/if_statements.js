/**
 * if(something){
 *  do this
 * } else {
 *  do this instead
 * }
 */

const id = 100;

// Equal to
if (id == 10) {
  console.log("Correct");
} else {
  console.log("incorrect");
}

// Not Equal to
if (id != 10) {
  console.log("Correct");
} else {
  console.log("incorrect");
}

// Equal to value & type
if (id === 100) {
  console.log("Correct");
} else {
  console.log("incorrect");
}
